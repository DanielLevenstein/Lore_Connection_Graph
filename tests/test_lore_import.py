def test_restored_backup_uses_snapshot_date_label_and_backup_date_sort(tmp_path, monkeypatch):
    lore_dir = tmp_path / "world_building" / "lore"
    meta_data_dir = tmp_path / "world_building" / "meta_data"
    backup_dir = tmp_path / "world_building" / "backup"
    character_file = lore_dir / "character_sheets" / "Jory_Ravenmark.md"
    character_file.parent.mkdir(parents=True)
    character_file.write_text("# Jory Ravenmark\n", encoding="utf-8")
    monkeypatch.setattr(lore_import, "META_DATA_DIR", meta_data_dir)
    monkeypatch.setattr(lore_import, "ensure_base_dirs", lambda: None)
    restored_snapshot_date = datetime(2026, 7, 12, 9, 30, tzinfo=timezone.utc)
    restored_backup_date = datetime(2026, 7, 13, 10, 30, tzinfo=timezone.utc)
    later_snapshot_date = datetime(2026, 7, 13, 9, 30, tzinfo=timezone.utc)

    restored = backup_lore_files(
        lore_dir,
        backup_dir,
        backup_date=restored_backup_date,
        snapshot_date=restored_snapshot_date,
        snapshot=True,
        backup_kind=lore_import.BACKUP_KIND_RESTORED,
    )
    later_snapshot = backup_lore_files(
        lore_dir,
        backup_dir,
        backup_date=later_snapshot_date,
        snapshot_date=later_snapshot_date,
        snapshot=True,
        backup_kind=BACKUP_KIND_SNAPSHOT,
    )
    backups = list_lore_backups(backup_dir)

    assert backups[0].path == restored.backup_dir
    assert backups[0].label.startswith("Restored 2026-07-12")
    assert backups[0].backup_date == restored_backup_date
    assert backups[0].snapshot_date == restored_snapshot_date
    assert read_backup_snapshot_date(restored.backup_dir) == restored_snapshot_date
    assert read_backup_date(restored.backup_dir) == restored_backup_date
    metadata = json.loads((restored.backup_dir / lore_import.LORE_BACKUP_METADATA).read_text(encoding="utf-8"))
    assert metadata["snapshot_date"] == restored_snapshot_date.isoformat(timespec="seconds")
    assert metadata["backup_date"] == restored_backup_date.isoformat(timespec="seconds")
    assert "snapshot_creation_date" not in metadata
    assert "created_at" not in metadata
    assert "updated_at" not in metadata
    assert backups[1].path == later_snapshot.backup_dir


