def test_restore_lore_backup_deletes_lore_missing_from_backup(tmp_path, monkeypatch):
    backup_dir = tmp_path / "world_building" / "backup"
    current_backup_dir = tmp_path / "world_building" / "current_backup"
    lore_dir = tmp_path / "world_building" / "lore"
    meta_data_dir = tmp_path / "world_building" / "meta_data"
    characters_dir = lore_dir / "character_sheets"
    places_dir = lore_dir / "places"
    session_notes_dir = lore_dir / "session_notes"
    stale_place = places_dir / "Stale_Place.md"
    backup_character = backup_dir / "character_sheets" / "Jory_Ravenmark.md"
    stale_place.parent.mkdir(parents=True)
    backup_character.parent.mkdir(parents=True)
    stale_place.write_text("# Stale Place\n", encoding="utf-8")
    backup_character.write_text("# Jory Ravenmark\n", encoding="utf-8")
    backup_source_snapshot_date = datetime(2026, 7, 12, 9, 30, tzinfo=timezone.utc)
    backup_lore_files(
        backup_dir,
        backup_dir,
        backup_date=backup_source_snapshot_date,
        snapshot_date=backup_source_snapshot_date,
    )
    timestamps = iter(
        [
            datetime(2026, 7, 18, 15, 41, tzinfo=timezone.utc),
            datetime(2026, 7, 18, 15, 42, tzinfo=timezone.utc),
        ]
    )
    monkeypatch.setattr(lore_import, "LORE_DIR", lore_dir)
    monkeypatch.setattr(lore_import, "GENERATED_LORE_DIR", lore_dir)
    monkeypatch.setattr(lore_import, "CHARACTER_METADATA_DIR", tmp_path / "data" / "character_metadata")
    monkeypatch.setattr(lore_import, "CHARACTERS_DIR", characters_dir)
    monkeypatch.setattr(lore_import, "PLACES_DIR", places_dir)
    monkeypatch.setattr(lore_import, "SESSION_NOTES_DIR", session_notes_dir)
    monkeypatch.setattr(lore_import, "META_DATA_DIR", meta_data_dir)
    monkeypatch.setattr(lore_import, "WORLD_BUILDING_BACKUP_DIR", current_backup_dir)
    monkeypatch.setattr(lore_import, "current_backup_timestamp", lambda: next(timestamps))
    monkeypatch.setattr(
        lore_import,
        "ensure_base_dirs",
        lambda: [
            path.mkdir(parents=True, exist_ok=True)
            for path in (characters_dir, places_dir, session_notes_dir, lore_dir, meta_data_dir)
        ],
    )

    summary = restore_lore_backup(backup_dir)

    assert summary.characters == 1
    assert (characters_dir / "Jory_Ravenmark.md").exists()
    assert not stale_place.exists()
    backups = list_lore_backups(current_backup_dir)
    assert backups[0].label.startswith("Restored ")
    assert backups[1].label.startswith("Snapshot ")
    assert backups[0].snapshot_date == backup_source_snapshot_date
    assert backups[0].backup_date == datetime(2026, 7, 18, 15, 42, tzinfo=timezone.utc)
    assert backups[1].snapshot_date == datetime(2026, 7, 18, 15, 41, tzinfo=timezone.utc)
    assert backups[1].backup_date == datetime(2026, 7, 18, 15, 41, tzinfo=timezone.utc)
    assert (backups[0].path / "character_sheets" / "Jory_Ravenmark.md").exists()
    assert (backups[1].path / "places" / "Stale_Place.md").exists()


