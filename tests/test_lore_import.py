def minimal_graph_json(name: str, source_file: str) -> str:
    node_id = name.lower().replace(" ", "_")
    return json.dumps(
        {
            "schema_version": "0.3.0",
            "primary_character": {
                "id": node_id,
                "name": name,
                "source_file": source_file,
            },
            "characters": {
                node_id: {
                    "name": name,
                    "role": "primary character",
                    "summary": "",
                    "source_spans": [],
                }
            },
            "attributes": {},
            "places": {},
            "relationships": [],
            "evidence": [],
            "embeddings": {},
        },
        indent=2,
    )


